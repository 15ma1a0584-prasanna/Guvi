

import java.util.*;
import java.lang.*;
import java.io.*;

class String4
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		StringBuffer sb=new StringBuffer();
		String str=sc.nextLine();
		sb.append(".");
		System.out.println(str+sb.toString());
	}
}
